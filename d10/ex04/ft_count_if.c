int	ft_count_if(char **tab, int (*f)(char *))
{
	int cnt;
	int i;

	i = 0;
	cnt = 0;
	while (tab[i] != 0)
	{
		if ((*f)(tab[i]) == 1)
			cnt++;
		i++;
	}
	return (cnt);
}
